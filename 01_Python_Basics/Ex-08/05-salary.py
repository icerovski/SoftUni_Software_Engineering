open_tabs_number = int(input())
salary = float(input())

while salary > 0:
    for i in range(0, open_tabs_number):
        tab_name = input()
    
        if tab_name == 'Facebook': salary -= 150
        elif tab_name == 'Instagram': salary -= 100
        elif tab_name == 'Reddit': salary -= 50
    
    
    
    
    
